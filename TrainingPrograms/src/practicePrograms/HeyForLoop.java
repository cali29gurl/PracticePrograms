package practicePrograms;

public class HeyForLoop {

	public static void main(String[] args) {
		// For loop with Hey with 5th saying que pasa
		for (int i = 0; i <= 20; i++) {

			if (i % 5 == 0) {

				System.out.println("que pasa");
			} else {

				System.out.println("hey");
			}
		}
	}

}
